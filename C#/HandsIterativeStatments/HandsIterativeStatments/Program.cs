using System;

namespace HandsIterativeStatments
{
    class Program
    {
        static void Main(string[] args)
        {
            //while loop
            int n = 1;
            //print no's from 1 to 100
            while(n<=100)
            {
                Console.Write(n + " ");
                n++;
            }
            Console.WriteLine("Hello World!");
        }
    }
}
